import UIKit

extension UIImage {
    public class var movies: UIImage {
        return UIImage(named: "movies")!
    }

    public class var logo: UIImage {
        return UIImage(named: "logo")!
    }

    public class var banner: UIImage {
        return UIImage(named: "banner-example")!
    }
}
